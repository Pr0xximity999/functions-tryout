def addition(num1:float, num2:float):
    print(f"{num1} + {num2} = {num1 + num2}")

def subtraction(num1:float, num2:float):
    print(f"{num1} - {num2} = {num1 - num2}")

def multiplication(num1:float, num2:float):
    print(f"{num1} x {num2} = {num1 * num2}")

def division(num1:float, num2:float):
    print(f"{num1} / {num2} = {num1 / num2}")

def increment(num1:float):
    print(f"{num1} + 1 = {num1 + 1}")

def decrement(num1:float):
    print(f"{num1} - 1 = {num1 - 1}")

addition(10, 12)
subtraction(58, 34)
multiplication(6, 7)
division(144, 12)
increment(12)
decrement(34)
